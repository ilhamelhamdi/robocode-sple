package elhamdi;

public class Adudu {
	private String headShape;

	public void setHeadShape() {
		this.headShape = "Square";
		System.out.println("Head shape set to: " + this.headShape);
	}

	public String getHeadShape() {
		return this.headShape;
	}

	private String color;

	public void setColor() {
		this.color = "Green";
		System.out.println("Color set to Green");
	}

	public String getColor() {
		return this.color;
	}
}
